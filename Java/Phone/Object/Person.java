package Java.Phone;

public class Person {

    private final Phone ph = new Phone();

    public void on () {
        ph.on();
    }

    public void off () {
        ph.off();
    }

    public void unlock () {
        ph.unlock();
    }

    public void app () {
        ph.app();
    }

    public void appEnd () {
        ph.appEnd();
    }
}
